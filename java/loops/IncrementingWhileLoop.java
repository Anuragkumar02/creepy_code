class Coffee {
  
  public static void main(String[] args) {
    
    // initialize cupsOfCoffee
    int cupsOfCoffee = 1;
    
    // add while loop with counter
    while(cupsOfCoffee<=100)
    {
      String cool = "Fry drinks cup of coffee #"+ cupsOfCoffee;
      System.out.println(cool);
      cupsOfCoffee++;
    }
        
    
  }
  
}
